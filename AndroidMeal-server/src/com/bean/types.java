package com.bean;

/**
 * 类型实体类
 * @author dda
 *
 */
public class types {
	//id值
	private int id;
	//类型的名字
	private String typename;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTypename() {
		return typename;
	}
	public void setTypename(String typename) {
		this.typename = typename;
	}
	
	
	
}
