package com.bean;

/**
 * 座位实体类
 * @author dda
 *
 */
public class seats {
//id值
private int id;
//座位序号
private int no;
//座位的状态
private int state;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getNo() {
	return no;
}
public void setNo(int no) {
	this.no = no;
}
public int getState() {
	return state;
}
public void setState(int state) {
	this.state = state;
}

}
